fun main() {
    var num1: Int = 34
    var num2: Int = 15
    var sum = num1 + num2
    println(sum)
    println(num1 * num2)
    println(num1 - num2)
    println(num1 / num2)
    println(num1 % num2)


//Assignment operators
    var number = 5
    println(number)


//Comparison operators
    var firstnumber = 89
    var secondnumber = 64
    println(firstnumber<secondnumber)
    println(firstnumber>secondnumber)
    println(firstnumber==secondnumber)
    println(firstnumber<=secondnumber)
    println(firstnumber>=secondnumber)
    println(firstnumber!=secondnumber)


//Logical operators
var log=78
println(log<56 && log>23) //And
println(log<56 || log>23) //Or
println(!(log<56 || log>23)) //Not




}